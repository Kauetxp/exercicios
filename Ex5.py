menu = str(input("0 - FIM\n1 - Inclui\n2 - Altera\n3 - Exclui\n4 - Consulta\n"))



if menu == "0":
    print("FIM")
elif menu == "1":
    print("INCLUI")
elif menu == "2":
    print("ALTERA")
elif menu == "3":
    print("EXCLUI")
elif menu == "4":
    print("CONSULTA")
else:
    print("Digito invalido")

